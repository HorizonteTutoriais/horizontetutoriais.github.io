const HorizonteData = {
    items: items,
    loadItemsFromStorage() {
        const stored = localStorage.getItem('horizonte_items');

        if (stored) {
            try {
                const localItems = JSON.parse(stored);
                const fileIds = new Set(this.items.map(i => i.id));
                localItems.forEach(item => { if (!fileIds.has(item.id)) this.items.push(item); });
            } catch (e) { console.error("Erro:", e); }
        }
    },
getAllItems() { 
        return this.items; 
    },
    getLatestUpdates() { 
        return this.items.slice(0, 8); 
    },
    getHighlights() { 
        return this.items.filter(i => i.exibir?.emDestaques).slice(0, 8);
    },
    getPopular() { 
        return this.items.filter(i => i.exibir?.emPopulares).slice(0, 8);
    }
};
